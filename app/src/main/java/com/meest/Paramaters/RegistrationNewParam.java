package com.meest.Paramaters;

public class RegistrationNewParam {
    String firstName;
    String lastName;
    String password;
    String username;
    String gender;
    String mobile;
    String email;
    String status;
    String dob;

    public RegistrationNewParam(String firstName, String lastName, String password, String username, String gender, String mobile, String email, String status, String dob) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.password = password;
        this.username = username;
        this.gender = gender;
        this.mobile = mobile;
        this.email = email;
        this.status = status;
        this.dob = dob;
    }
}
