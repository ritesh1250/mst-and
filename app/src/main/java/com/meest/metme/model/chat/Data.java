package com.meest.metme.model.chat;

public class Data {

    public String id;
    public String status;
    public String userid;
    public String group;
    public String video;
    public String callRoomId;
    public String jitsiToken;
    public String url;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getGroup() {
        return group;
    }

    public void setGroup(String group) {
        this.group = group;
    }

    public String getVideo() {
        return video;
    }

    public void setVideo(String video) {
        this.video = video;
    }

    public String getCallRoomId() {
        return callRoomId;
    }

    public void setCallRoomId(String callRoomId) {
        this.callRoomId = callRoomId;
    }

    public String getJitsiToken() {
        return jitsiToken;
    }

    public void setJitsiToken(String jitsiToken) {
        this.jitsiToken = jitsiToken;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(String updatedAt) {
        this.updatedAt = updatedAt;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    public String updatedAt;
    public String createdAt;

}
